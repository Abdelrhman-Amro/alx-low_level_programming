#include "3-calc.h"

/**
 * op_add - add numbers
 * @a: num 1
 * @b: num 2
 * Return: Integer
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub numbers
 * @a: num 1
 * @b: num 2
 * Return: Integer
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mul numbers
 * @a: num 1
 * @b: num 2
 * Return: Integer
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - div numbers
 * @a: num 1
 * @b: num 2
 * Return: Integer
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod numbers
 * @a: num 1
 * @b: num 2+
 * Return: Integer
*/
int op_mod(int a, int b)
{
	return (a % b);
}
