#include "main.h"

/**
 * main - entry point
 *
 * Description: print_alphabet
 *
 * Return: Always 0
*/

/**
 * print_alphabet(void) - print alphabet
 *
 * Description: print alphabet
 *
 * Return: none
*/

void print_alphabet(void)
{
	char i;

	for (i = 'a'; i <= 'z'; i++)
		_putchar(i);

	_putchar('\n');
}
